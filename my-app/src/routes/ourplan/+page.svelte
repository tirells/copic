			Overview:<br>
			The New York City Charter establishes a Commission on Public Information and Communication (CoPIC) with the Public Advocate as chair and members including representatives from corporate council, the Mayor's Director of Operations, Department of Records Commissioner, WNYC President, one City Council member selected by the City Council, two additional appointees from the Mayor's office (focused on information and media), one additional appointee from the Public Advocate's Office, and one appointee from the Borough Presidents (collectively chosen). The commission is tasked with appointing an executive director, general counsel  and other officers to meet the tasks of the commission However, due to lack of funding, these positions have remained vacant, and the commission has been inactive, with only an interim executive director appointed from the Public Advocate's office.<br><br>

			Stated Directives of CoPIC:<br>
			The commission's stated mission, "help make city government more transparent to the citizens it serves and to encourage the public to become more actively involved in government," is narrow compared to the broader directives outlined in the charter. These directives include:<br>
			<ol>
				<li>Educating the public about city-produced or maintained information and assisting in accessing it.</li>
				<li>Reviewing city information policies (digital and non-digital), including public access to information, quality, structure and costs.</li>
				<li>Ensuring agency compliance with notice, comment and hearing provisions.</li>
				<li>Assessing the usefulness and availability of city documents, reports and publications.</li>
				<li>Assisting city agencies in facilitating public access to meetings, transcripts, records and other information.</li>
				<li>Monitor agency compliance with the provisions of the charter and other laws which require public access to information.</li>
				<li>Holding public hearings and issuing reports with recommendations.</li>
			</ol>

			Recent History and Current Status of CoPIC:<br>
			Since 2016/2015, CoPIC has been inactive, with only sporadic meetings. In its heyday CoPIC had published reports in 1993 and 2001. Its effect had convinced the Department of Sanitation to have a transparent website. Efforts to address issues related to public hearings accessibility were made during that time but lacked substantial commitment from commission members. Despite this, ongoing efforts by the former deputy general counsel and a representative from DoITT focused on technological solutions for streaming public hearings and developing the CoPIC website. Additionally, a proposal for a centralized city-wide hearing portal using blockchain technology was introduced as a bill (LS 7209) in 2018.<br><br>

			Next Steps for CoPIC:<br>
			To revive CoPIC, the first steps include verifying board members and appointing an interim executive director. Simultaneously, efforts should resume on completing the CoPIC website, possibly under its own domain (copic.nyc.gov), for better control and visibility. Once the infrastructure is in place, the commission can focus on appointing a full executive director and pursuing its directives.<br><br>

			Full Year 2024 | 2025 Plan:<br>
			For 2024 | 2025, CoPIC can focus on four main projects:<br>
			<ol>
				<li>Establishing the cadence of Member Meetings and Publics Hearings</li>
				<li>Continuing research on a city-wide hearing portal; @ City Council or 1 Centre Street in room 2209.</li>
				<li>Conducting an audit of city agencies' data, information, technology and communication systems.</li>
				<li>Creating an educational platform for constituents on navigating city data and information.</li>
				<li>Ensuring agency usage of modern, compliant, and secure technology and data policies</li>
			</ol>

			Potential of CoPIC:<br>
			As the chair of CoPIC, the Public Advocate's office can play a crucial role in monitoring and improving city-wide data, information, technology and communication systems. CoPIC's directives align closely with the office's duties, providing opportunities for collaboration and synergy. With dedicated resources and attention, CoPIC can ensure transparency, accessibility, and continuous improvement in city governance.<br><br>

			Existing and Potential Roadblocks:<br>
			Key challenges include funding shortages and indifference from city agencies. Despite advancements like the open data law, CoPIC remains necessary for adapting to the evolving landscape of data and ensuring enforcement and oversight of city agencies.<br><br>

			Conclusion:<br>
			CoPIC, in partnership with the Public Advocate's office, has the potential to be a unifying force for transparency and accountability in city government and meet the needs of the people who voted for its creation in 1989. By addressing foundational issues, setting clear goals and securing resources, CoPIC can fulfill its directives and contribute to better governance in New York City.<br><br>
