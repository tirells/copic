    <h1><b>ยง 1061. Commission on public information and communication.</b></h1>
    <p><b>a.</b> There shall be a commission on public information and communication which shall consist of the public advocate, as chair, the corporation counsel or the delegate of such officer, the director of operations or the delegate of such officer, the commissioner of the department of records and information services or the delegate of such officer, the commissioner of information technology and telecommunications or the delegate of such officer, the president of the WNYC communications group or the delegate of such officer, and one council member elected by the council, all of whom shall serve on the board without compensation. In addition, there shall be four other members, each appointed for a four-year term, who shall not hold or seek public or political party office or be public employees in any jurisdiction, except the representative of the community board as set forth herein, to be appointed as follows: two by the mayor, one of whom is or has been a representative of the news media and one of whom shall be a member of a community board; one by the public advocate; and one by the boroughpresidents acting as a group. Such members shall receive a per diem compensation for each calendar day they perform the work of the commission. No such members shall serve for more than two consecutive four-year terms. All initial appointments shall be made by the first day of March, nineteen hundred ninety.</p>
    <p><b>b.</b> Members may be removed by the mayor for cause after notice and opportunity to be heard. Members shall serve until their successors have been appointed.</p>
    <p><b>c.</b> The commission shall appoint an executive director, a general counsel and such other officers, employees, and consultants as are necessary to fulfill its duties, within appropriations available therefor.</p>
    <p><b>d.</b> The commission shall:</p>
        <p class="list">(1) undertake, by itself, or in cooperation
        with other entities, activities to educate the public about the
        availability and potential usefulness of city produced or maintained
        information and assist the public in obtaining access to such
        information;</p>
        <p class="list">(2) review (i) all city information policies, including but not
        limited to, policies regarding public access to city produced or
        maintained information, particularly, computerized information; (ii) the
        quality, structure, and costs to the public of such information; (iii) 
        agency compliance with the various notice, comment, and hearing
        provisions of the charter and other laws applicable to city agencies;
        and (iv) the usefulness and availability of city documents, reports, and
        publications;</p>
        <p class="list">(3) assist city agencies in facilitating public access to
        their meetings, transcripts, records, and other information, and monitor
        agency compliance with the provisions of the charter, and other laws
        which require such public access;</p>
        <p class="list">(4) hold at least one public hearing each year on city information
        policies and issue at least one report each year with such
        recommendations as the commission deems advisable;</p>
        <p class="list">(5) on the request of any member of the public, elected official, or
        city agency, render advisory opinions regarding the application of those
        provisions of the charter or other laws which require public access to
        meetings, transcripts, records and other information. Such advisory
        opinions shall be indexed by subject matter and maintained on a
        cumulative basis;</p>
        <p class="list" id="d6">(6) make recommendations regarding: (i) the application of new
        communications technology to improve public access to city produced or
        maintained information; (ii) the distribution of information to the
        public about the purposes and locations of the city's service delivery
        facilities; and (iii) programming for the municipal cable channels and
        broadcasting system.</p>
